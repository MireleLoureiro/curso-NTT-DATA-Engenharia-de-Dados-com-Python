numero_semana = int(input("Digite um número correspondente ao dia da semana (1-Domingo, 2-Segunda, 3-Terça, etc.): "))

if numero_semana == 1:
    print("Domingo")
elif numero_semana == 2:
    print("Segunda")
elif numero_semana == 3:
    print("Terça")
elif numero_semana == 4:
    print("Quarta")
elif numero_semana == 5:
    print("Quinta")
elif numero_semana == 6:
    print("Sexta")
elif numero_semana == 7:
    print("Sábado")
else:
    print("Valor inválido")